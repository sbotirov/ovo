class Multilingual {

    /**
     * Spanish: Que hace esto?
     */
    fun add(a: Int, b: Int): Int {
        return a + b
    }

    /**
     * German: Was macht dieser Code?
     */
    fun subtract(a: Int, b: Int): Int {
        return a - b
    }

    /**
     * Hindi: yah kod kya karata hai?
     */
    fun multiply(a: Int, b: Int): Int {
        return a * b
    }
}